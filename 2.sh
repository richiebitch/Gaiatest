            PORT=$((8080 + NODE_NUMBER))

            # Check if the gaianet binary exists
            if [ -f "$dir/bin/gaianet" ]; then
                STATUS="‚úÖ Installed"
            else
                STATUS="‚ùå Not Installed"
            fi

            # Display node information
            echo "Node $NODE_NUMBER:"
            echo "  Directory: $dir"
            echo "  Port: $PORT"
            echo "  Status: $STATUS"
            echo "----------------------------------------"
        fi
    done
}

