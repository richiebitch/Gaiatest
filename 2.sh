        CONFIG_URL="https://raw.githubusercontent.com/abhiag/Gaia_Node/main/config2.json"
    fi
    echo "üîó Using configuration: $CONFIG_URL"
}

# Function to install CUDA Toolkit 12.8 in WSL or Ubuntu 24.04
install_cuda() {
    if $IS_WSL; then
        echo "üñ•Ô∏è Installing CUDA for WSL 2..."
        # Define file names and URLs for WSL
        PIN_FILE="cuda-wsl-ubuntu.pin"
        PIN_URL="https://developer.download.nvidia.com/compute/cuda/repos/wsl-ubuntu/x86_64/cuda-wsl-ubuntu.pin"
        DEB_FILE="cuda-repo-wsl-ubuntu-12-8-local_12.8.0-1_amd64.deb"
        DEB_URL="https://developer.download.nvidia.com/compute/cuda/12.8.0/local_installers/cuda-repo-wsl-ubuntu-12-8-local_12.8.0-1_amd64.deb"
    else
        echo "üñ•Ô∏è Installing CUDA for Ubuntu 24.04..."
        # Define file names and URLs for Ubuntu 24.04
        PIN_FILE="cuda-ubuntu2404.pin"
        PIN_URL="https://developer.download.nvidia.com/compute/cuda/repos/ubuntu2404/x86_64/cuda-ubuntu2404.pin"
        DEB_FILE="cuda-repo-ubuntu2404-12-8-local_12.8.0-570.86.10-1_amd64.deb"
        DEB_URL="https://developer.download.nvidia.com/compute/cuda/12.8.0/local_installers/cuda-repo-ubuntu2404-12-8-local_12.8.0-570.86.10-1_amd64.deb"
    fi

    # Download the .pin file
    echo "üì• Downloading $PIN_FILE from $PIN_URL..."
    wget "$PIN_URL" || { echo "‚ùå Failed to download $PIN_FILE from $PIN_URL"; exit 1; }

    # Move the .pin file to the correct location
    sudo mv "$PIN_FILE" /etc/apt/preferences.d/cuda-repository-pin-600 || { echo "‚ùå Failed to move $PIN_FILE to /etc/apt/preferences.d/"; exit 1; }

    # Remove the .deb file if it exists, then download a fresh copy
    if [ -f "$DEB_FILE" ]; then
        echo "üóëÔ∏è Deleting existing $DEB_FILE..."
        rm -f "$DEB_FILE"
    fi
    echo "üì• Downloading $DEB_FILE from $DEB_URL..."
    wget "$DEB_URL" || { echo "‚ùå Failed to download $DEB_FILE from $DEB_URL"; exit 1; }

    # Install the .deb file
    sudo dpkg -i "$DEB_FILE" || { echo "‚ùå Failed to install $DEB_FILE"; exit 1; }

    # Copy the keyring
    sudo cp /var/cuda-repo-*/cuda-*-keyring.gpg /usr/share/keyrings/ || { echo "‚ùå Failed to copy CUDA keyring to /usr/share/keyrings/"; exit 1; }

    # Update the package list and install CUDA Toolkit 12.8
    echo "üîÑ Updating package list..."
    sudo apt-get update || { echo "‚ùå Failed to update package list"; exit 1; }
    echo "üîß Installing CUDA Toolkit 12.8..."
    sudo apt-get install -y cuda-toolkit-12-8 || { echo "‚ùå Failed to install CUDA Toolkit 12.8"; exit 1; }

    echo "‚úÖ CUDA Toolkit 12.8 installed successfully."
    setup_cuda_env
}

# Set up CUDA environment variables
setup_cuda_env() {
    echo "üîß Setting up CUDA environment variables..."
    echo 'export PATH=/usr/local/cuda-12.8/bin${PATH:+:${PATH}}' | sudo tee /etc/profile.d/cuda.sh
    echo 'export LD_LIBRARY_PATH=/usr/local/cuda-12.8/lib64${LD_LIBRARY_PATH:+:${LD_LIBRARY_PATH}}' | sudo tee -a /etc/profile.d/cuda.sh
    source /etc/profile.d/cuda.sh
}

# Function to install GaiaNet with or without CUDA support
install_gaianet() {
    local BASE_DIR=$1
    local CONFIG_URL=$2

    # Create the base directory if it doesn't exist
    if [ ! -d "$BASE_DIR" ]; then
        echo "üìÇ Creating directory $BASE_DIR..."
        mkdir -p "$BASE_DIR" || { echo "‚ùå Failed to create directory $BASE_DIR"; exit 1; }
    fi

    # Check for CUDA support
    if command -v nvcc &> /dev/null; then
        CUDA_VERSION=$(nvcc --version | grep -oP 'release \K\d+\.\d+' | cut -d. -f1)
        echo "‚úÖ CUDA version detected: $CUDA_VERSION"
        
        if [[ "$CUDA_VERSION" == "11"* || "$CUDA_VERSION" == "12"* ]]; then
            echo "üîß Installing GaiaNet with CUDA support..."
            curl -sSfL 'https://github.com/GaiaNet-AI/gaianet-node/releases/download/0.4.20/install.sh' | bash -s -- --base "$BASE_DIR" --ggmlcuda "$CUDA_VERSION" || { echo "‚ùå GaiaNet installation failed."; exit 1; }
            return
        fi
    fi

    echo "‚ö†Ô∏è Installing GaiaNet without GPU support..."
    curl -sSfL 'https://github.com/GaiaNet-AI/gaianet-node/releases/download/0.4.20/install.sh' | bash -s -- --base "$BASE_DIR" || { echo "‚ùå GaiaNet installation failed."; exit 1; }

    # Download and apply the configuration file
    echo "üì• Downloading configuration from $CONFIG_URL..."
    wget -O "$BASE_DIR/config.json" "$CONFIG_URL" || { echo "‚ùå Failed to download configuration file."; exit 1; }
}

# Function to install GaiaNet node
install_gaianet_node() {
    local NODE_NUMBER=$1
    local CONFIG_URL=$2
    local BASE_DIR="$HOME/gaianet$NODE_NUMBER"
    local PORT=$((8081 + NODE_NUMBER - 1))

    echo "üîß Setting up GaiaNet Node $NODE_NUMBER in $BASE_DIR on port $PORT..."

    # Check if GaiaNet is already installed
    if [ -f "$BASE_DIR/bin/gaianet" ]; then
        echo "‚ÑπÔ∏è GaiaNet is already installed in $BASE_DIR. Skipping installation."
    else
        # Install GaiaNet with the provided CONFIG_URL
        install_gaianet "$BASE_DIR" "$CONFIG_URL"

        # Verify installation
        if [ -f "$BASE_DIR/bin/gaianet" ]; then
            echo "‚úÖ GaiaNet Node $NODE_NUMBER installed successfully in $BASE_DIR."
        else
            echo "‚ùå GaiaNet Node $NODE_NUMBER installation failed."
            return 1
        fi
    fi

    # Configure port
    "$BASE_DIR/bin/gaianet" config --base "$BASE_DIR" --port "$PORT" || { echo "‚ùå Port configuration failed."; return 1; }

    # Initialize and start the node
    "$BASE_DIR/bin/gaianet" init --base "$BASE_DIR" || { echo "‚ùå GaiaNet initialization failed!"; return 1; }
    "$BASE_DIR/bin/gaianet" start --base "$BASE_DIR" || { echo "‚ùå Error: Failed to start GaiaNet node!"; return 1; }

    echo "üéâ GaiaNet Node $NODE_NUMBER successfully installed and started in $BASE_DIR on port $PORT!"
}
# Function to start a specific node
