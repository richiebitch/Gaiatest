    fi
}

# Function to check if an NVIDIA GPU is present
check_nvidia_gpu() {
    if command -v nvidia-smi &> /dev/null || lspci | grep -i nvidia &> /dev/null; then
        echo "‚úÖ NVIDIA GPU detected."
        return 0
    else
        echo "‚ö†Ô∏è No NVIDIA GPU found."
        return 1
